ソフトウェアを開発を今よりもより良くするには、ソフトウェア技術者や研究者が社会組織(*1)の壁を越えて交流を深め、議論することが大事だと思っています。このサイトでは、定期的に開催されるイベントや各種団体の情報をお知らせします。かなり偏った情報ですので、あらかじめご了承ください。<br/><br/>

(*1) 社会組織の壁：英語では、“Social/Organizational Barrier”。会社も含めて、学会、業界団体、政府関係機関、そうした「社会組織の壁」を意味しています（<a href="http://www.jaspic.org/organization/policy/">JASPIC</a>のサイトから引用）。<br/>

(*2) 日程や会場が間違っている場合があるかもしれません。正確な情報は各イベントのWebサイトなどで確認してください。<br/><br/>
